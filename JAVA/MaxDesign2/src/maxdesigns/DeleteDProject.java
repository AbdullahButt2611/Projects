    public String fileData(int index, String text)
    {
        String s = "\n\n*****************************************************************************\n";
               s += "                               Delete Project\n";
               s += "____________________________________________________________________________\n";
                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");  
                LocalDateTime now = LocalDateTime.now(); 
               s += "Date/Time : "+dtf.format(now)+"                        Ayesha | Accountant\n\n";
               s += "Deleted Account Details................................... \n\n";
               s += "Project Name : "+MaxDesigns.getInstance().getDes().get(index).getName()+" ( "+MaxDesigns.getInstance().getDes().get(index).getId()+" )\n";
               s += "Status : "+MaxDesigns.getInstance().getDes().get(index).getStatus()+"\n";
               s += "Services : "+MaxDesigns.getInstance().getDes().get(index).getServices()+"\n";
               s += "Balance : "+MaxDesigns.getInstance().getDes().get(index).getBalance()+"\n\n\n";
               s += "Deleted by Option : "+text+"\n";
               s += "                                                 MAX DESIGNS\n";
               s += "__________________________________________________________________________________________\n";
               return s;
    }
    
    
