    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        // TODO add your handling code here:\
        String text = "";
        if(ownerBtn.isSelected())
        {
            text = ownerTxt.getText();
            if(text.equals("") || text.equals(null))
            {
                JOptionPane.showMessageDialog(this,"The Owner Name Input is empty","Empty Fields",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                int index = -1;
                for(int i = 0;i<MaxDesigns.getInstance().getDes().size();i++)
                {
                    if(MaxDesigns.getInstance().getDes().get(i).getOwner().equals(text))
                    {
                        index  = i;
                        break;
                    }
                    else
                        index  = -1;
                }
                if(index == -1)
                {
                    JOptionPane.showMessageDialog(this,"The Owner Name Not Found","Error",JOptionPane.ERROR_MESSAGE);
                }
                else
                {
                    MaxDesigns.getInstance().DrawWrite(fileData(index,"By Owner Name"));
                    MaxDesigns.getInstance().SeperateDraw(MaxDesigns.getInstance().getDes().get(index).getName(),FullData());
                    MaxDesigns.getInstance().getDes().remove(index);
                    JOptionPane.showMessageDialog(this,"The Data removed Succesfully","Congratulations",JOptionPane.INFORMATION_MESSAGE);
                    MaxDesigns.getInstance().writeDes();
                }
            }
        }
        else if(idBtn.isSelected())
        {
            text = idTxt.getText();
            if(text.equals("") || text.equals(null))
            {
                JOptionPane.showMessageDialog(this,"The ID Input is empty","Empty Fields",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                int index = -1;
                for(int i = 0;i<MaxDesigns.getInstance().getDes().size();i++)
                {
                    if(MaxDesigns.getInstance().getDes().get(i).getId().equals(text))
                    {
                        index  = i;
                        break;
                    }
                    else
                        index  = -1;
                }
                if(index == -1)
                {
                    JOptionPane.showMessageDialog(this,"The ID Not Found","Error",JOptionPane.ERROR_MESSAGE);
                }
                else
                {
                    MaxDesigns.getInstance().DrawWrite(fileData(index,"By ID"));
                    MaxDesigns.getInstance().SeperateDraw(MaxDesigns.getInstance().getDes().get(index).getName(),FullData());
                    MaxDesigns.getInstance().getDes().remove(index);
                    JOptionPane.showMessageDialog(this,"The Data removed Succesfully","Congratulations",JOptionPane.INFORMATION_MESSAGE);
                    MaxDesigns.getInstance().writeDes();
                }
            }
        }
        else if(nameBtn.isSelected())
        {
            text = nameTxt.getText();
            if(text.equals("") || text.equals(null))
            {
                JOptionPane.showMessageDialog(this,"The Name Input is empty","Empty Fields",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                int index = -1;
                for(int i = 0;i<MaxDesigns.getInstance().getDes().size();i++)
                {
                    if(MaxDesigns.getInstance().getDes().get(i).getName().equals(text))
                    {
                        index  = i;
                        break;
                    }
                    else
                        index  = -1;
                }
                if(index == -1)
                {
                    JOptionPane.showMessageDialog(this,"The Project Name Not Found","Error",JOptionPane.ERROR_MESSAGE);
                }
                else
                {
                    MaxDesigns.getInstance().DrawWrite(fileData(index,"By Project Name"));
                    MaxDesigns.getInstance().SeperateDraw(MaxDesigns.getInstance().getDes().get(index).getName(),FullData());
                    MaxDesigns.getInstance().getDes().remove(index);
                    JOptionPane.showMessageDialog(this,"The Data removed Succesfully","Congratulations",JOptionPane.INFORMATION_MESSAGE);
                    MaxDesigns.getInstance().writeDes();
                }
            }
        }
        else
            JOptionPane.showMessageDialog(this,"You First need to choose on of the Delete Types by clicking on radio buttons","Radio Button Not Selected",JOptionPane.ERROR_MESSAGE);
            
    }//GEN-LAST:event_delBtnActionPerformed


