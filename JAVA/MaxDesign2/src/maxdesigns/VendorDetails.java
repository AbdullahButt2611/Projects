    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        String sale = saleTxt.getText();
        String project = projectTxt.getText();
        String details = detailTxt.getText();
        
        if(sale.equals("") || sale.equals(null) || project.equals("") || project.equals(null) || details.equals("") || details.equals(null))
        {
            if(sale.equals("") || sale.equals(null))
            {
                invalidSale.setText("This field is Empty");
            }
            else
                invalidSale.setText("");
            
            if(project.equals("") || project.equals(null))
            {
                proTxt.setText("This field is Empty");
            }
            else
                proTxt.setText("");
            
            if(details.equals("") || details.equals(null))
            {
              JOptionPane.showMessageDialog(this,"You need to enter Some details First","Empty Area",JOptionPane.ERROR_MESSAGE);  
            }
        }
        else
        {
            int Oindex = -1;
            
            if(isValidName(project))
            {
                proTxt.setText("");
                addData(project);
                //System.out.print("The data is "+data);
                if(MaxDesigns.getInstance().getVen().get(index).isDigits(sale))
                {
                    
                    invalidSale.setText("");
                    int am = Integer.parseInt(sale);
                    if(am > 0)
                    {
                        invalidSale.setText("");
                        double send = am * (MaxDesigns.getInstance().getVen().get(index).getSharePer()/100);
                        int s = (int)send;
                        if(MaxDesigns.getInstance().getVen().get(index).addMoney(s))
                        {
                            data += "The Amount of which the profit has been granted is Rs : "+sale+"\n";
                            invalidSale.setText("");
                            data += "The Details are as follows :\n\n";
                            data += details;
                            data += "\n\n                                               MAX DESIGNS\n";
                            data += "_________________________________________________________________________________\n\n";
                            MaxDesigns.getInstance().VenWrite(data);
                            MaxDesigns.getInstance().writeVen();
                            JOptionPane.showMessageDialog(this,"Data has been Added Successfully","Congratulations :)",JOptionPane.INFORMATION_MESSAGE);
                            saleTxt.setText("");
                            projectTxt.setText("");
                            detailTxt.setText("");
                            vendorTxt.setText("");
                            saleTxt.setEnabled(false);
                            projectTxt.setEnabled(false);
                            detailTxt.setEnabled(false);
                            submitBtn.setEnabled(false);
                            vendorTxt.setEnabled(true);
                            srchBtn.setEnabled(true);
                        }
                        else
                            invalidSale.setText("The amount should be greater than 0");
                    }
                    else
                    invalidSale.setText("The amount should be greater than 0");
                }
                else
                    invalidSale.setText("The amount should be in digits");
                
            }
            else
                proTxt.setText("Only Alphabets and Spaces are Allowed");
        }
    }//GEN-LAST:event_submitBtnActionPerformed


