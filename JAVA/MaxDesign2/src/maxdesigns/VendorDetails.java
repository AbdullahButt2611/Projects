    public boolean isValidName(String name)
    {
        int size = name.length();
        boolean flag=false;
        
        for(int i =0;i<size;i++)
        {
            if((name.charAt(i)>='A' && name.charAt(i)<='Z')||(name.charAt(i)>='a' && name.charAt(i)<='z')||(name.charAt(i)==' ') || (name.charAt(i)>='0' && name.charAt(i)<='9') || (name.charAt(i)=='-'))
            {
                flag = true;
            }
            else
                return false;
        }
        return flag;
        
    }
    
